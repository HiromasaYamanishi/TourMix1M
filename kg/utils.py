def extract_words_from_response(text: str) -> List[str]:
    """応答テキストから名詞・形容詞を抽出（順序保持）"""
    
    # パターン1: "1. 単語 - English" 形式
    pattern1 = r'\d+\.\s+([^\s-]+)\s+-'
    
    # パターン2: "名詞: 単語1、単語2、単語3" 形式
    pattern2 = r'(名詞|形容詞):\s*(.*?)(?=\n|$)'
    
    # パターン3: "ASSISTANT: 単語1, 単語2, 単語3" 形式 - 日本語対応
    pattern3 = r'ASSISTANT:\s*([^a-zA-Z]+?)(?=\s+llava|$)'
    
    # パターン4: リスト形式 ['単語1', '単語2', ...]
    pattern4 = r'\[(.*?)\]'
    
    # パターン5: "1. 単語" 形式（English部分なし）
    pattern5 = r'\d+\.\s*([^\d\n]+?)(?=\s*\d+\.|$)'
    
    # OrderedDictを使用して順序を保持しながら重複を除去
    words_dict = OrderedDict()
    
    def add_word(word):
        """単語を追加（クリーニングチェック付き）"""
        if (word 
            and len(word) < 20  # 異常に長い単語を除外
            and not re.search(r'[^\w\s\u3000-\u9FFF]', word)  # 日本語と英数字以外を含まない
            and '�' not in word  # 文字化けを含まない
            and word.lower() not in ['llava', 'caption', 'orig', 'user', 'assistant']  # メタ単語を除外
            and not word.isspace()  # 空白文字のみの文字列を除外
            and not word.endswith('の')):  # 「の」で終わる不完全な単語を除外
            words_dict[word] = None
    
    # パターン1の処理
    matches1 = re.finditer(pattern1, text)
    for match in matches1:
        word = match.group(1).strip()
        if word and word != '-':
            add_word(word)
    
    # パターン2の処理
    matches2 = re.finditer(pattern2, text)
    for match in matches2:
        word_list = match.group(2).split('、')
        for word in word_list:
            add_word(word.strip())
    
    # パターン3の処理
    matches3 = re.finditer(pattern3, text)
    for match in matches3:
        if match.group(1):
            word_list = re.split(r'[,、]', match.group(1))
            for word in word_list:
                add_word(word.strip())
    
    # パターン4の処理
    matches4 = re.finditer(pattern4, text)
    for match in matches4:
        if match.group(1):
            word_list = re.split(r'[\'"\s,]+', match.group(1))
            for word in word_list:
                add_word(word.strip())
    
    # パターン5の処理
    matches5 = re.finditer(pattern5, text)
    for match in matches5:
        if match.group(1):
            add_word(match.group(1).strip())
    
    # OrderedDictのキーをリストとして返す
    return list(words_dict.keys())